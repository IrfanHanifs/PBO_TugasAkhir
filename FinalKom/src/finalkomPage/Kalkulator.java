    private void btSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSActionPerformed
        if (!angka.isEmpty()) {
        double value = Math.toRadians(Double.parseDouble(angka)); // Mengonversi ke radian
        double result = Math.sin(value);
        teksTampil.setText(Double.toString(result));
        angka = Double.toString(result);
        }
    }//GEN-LAST:event_btSActionPerformed

