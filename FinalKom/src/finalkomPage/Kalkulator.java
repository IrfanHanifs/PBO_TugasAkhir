    private void numHasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_numHasActionPerformed
        switch (pilih){
            case 1:
                bil2 = Double.parseDouble(angka);
                jumlah = bil1+bil2;
                angka = Double.toString(jumlah);
                teksTampil.setText(angka);
                break;
                
            case 2:
                bil2 = Double.parseDouble(angka);
                jumlah = bil1-bil2;
                angka = Double.toString(jumlah);
                teksTampil.setText(angka);
                break;
                
            case 3:
                bil2 = Double.parseDouble(angka);
                jumlah = bil1*bil2;
                angka = Double.toString(jumlah);
                teksTampil.setText(angka);
                break;
                
            case 4:
                bil2 = Double.parseDouble(angka);
                jumlah = bil1/bil2;
                angka = Double.toString(jumlah);
                teksTampil.setText(angka);
                break;
                
            default:
                break;
        }
    }//GEN-LAST:event_numHasActionPerformed

